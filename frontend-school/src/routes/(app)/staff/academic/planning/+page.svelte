	async function handleExportActualCurriculum() {
		if (!canReadCoursePlan) {
			toast.error('а№„аёЎа№ҲаёЎаёөаёӘаёҙаё—аёҳаёҙа№ҢаёӘа№ҲаёҮаёӯаёӯаёҒаёӮа№үаёӯаёЎаё№аёҘа№ғаёҠа№үаёҲаёЈаёҙаёҮ');
