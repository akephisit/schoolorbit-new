			loading = false;
			return;
		}

		try {
			loading = true;
			const res = await getAcademicStructure();
			structure = res.data;

			// Default Year
			const activeYear = structure.years.find((y) => y.is_active) || structure.years[0];
			if (activeYear) {
				selectedYearId = activeYear.id;
				actualExportYearId = activeYear.id;
				await fetchClassrooms();
				await fetchClassrooms();
			}
			if (canManageCoursePlan) loadTeachers(); // Preload teachers for management flows
		} catch (e) {
			console.error(e);
			toast.error('โหลดข้อมูลตั้งต้นไม่สำเร็จ');
		} finally {
			loading = false;
		}
	}

	function openActualExportDialog() {
		if (!canReadCoursePlan) {
			toast.error('ไม่มีสิทธิ์ส่งออกข้อมูลใช้จริง');
			return;
		}

		actualExportYearId =
			actualExportYearId ||
			selectedYearId ||
			structure.years.find((year) => year.is_active)?.id ||
