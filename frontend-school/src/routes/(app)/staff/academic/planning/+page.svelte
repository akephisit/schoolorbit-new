			showDeleteDialog = false;
			await fetchCourses();
		} catch (e) {
			console.error(e);
			toast.error('ลบไม่สำเร็จ');
		} finally {
			submitting = false;
		}
	}

	// Auto-Generate Courses from Study Plan
	async function handleAutoGenerate() {
		if (!canManageCoursePlan) {
			toast.error('ไม่มีสิทธิ์สร้างรายวิชาอัตโนมัติ');
			return;
		}

		if (!selectedClassroomId || !selectedTermId || !currentClassroom) {
			toast.error('กรุณาเลือกห้องเรียนและภาคเรียน');
			return;
		}

		autoGenerating = true;
		try {
			const result = await generateCoursesFromPlan({
				classroom_id: selectedClassroomId,
				academic_semester_id: selectedTermId,
				skip_existing: true
			});

			showAutoGenerateDialog = false;
			const coursesAdded = result.courses_created ?? result.items.added_count;
			const activitiesAdded = result.activities_created;
			toast.success(`สร้างคอร์ส ${coursesAdded} + กิจกรรม ${activitiesAdded}`, {
				duration: 5000
			});

			await fetchCourses();
			await fetchClassroomActivities();
		} catch (e) {
			console.error(e);
			toast.error('สร้างรายวิชาอัตโนมัติไม่สำเร็จ: ' + (e instanceof Error ? e.message : ''));
		} finally {
			autoGenerating = false;
		}
	}

	async function openAddCourseDialog() {
		if (!canManageCoursePlan) {
			toast.error('ไม่มีสิทธิ์เพิ่มรายวิชา');
			return;
		}

		if (!selectedClassroomId || !selectedTermId || !currentClassroom) {
			toast.error('กรุณาเลือกห้องเรียนและภาคเรียน');
			return;
		}
		addCourseTab = currentClassroom.study_plan_version_id ? 'plan' : 'catalog';
		showAddCourseDialog = true;
		await loadAddCourseLists();
	}

	async function loadAddCourseLists() {
		if (!canManageCoursePlan) return;

		if (!currentClassroom || !selectedTermId || !selectedYearId) return;
		addCourseLoading = true;
		try {
			const semester = structure.semesters.find((s) => s.id === selectedTermId);
			const termStr = semester?.term ?? '';

			const promises: Promise<void>[] = [];

			// จาก plan — เฉพาะถ้าห้องผูก plan ไว้
			if (currentClassroom.study_plan_version_id && currentClassroom.grade_level_id && termStr) {
				promises.push(
					listStudyPlanSubjects({
						study_plan_version_id: currentClassroom.study_plan_version_id,
						grade_level_id: currentClassroom.grade_level_id,
						term: termStr
					}).then((res) => {
						planSubjectsForAdd = res.data;
					})
				);
			} else {
				planSubjectsForAdd = [];
			}

			// จากคลัง — ทั้งหมดที่ active ในปีนี้
			promises.push(
				listSubjects({
					active_only: true,
					active_in_year_id: selectedYearId
				}).then((res) => {
					catalogSubjectsForAdd = res.data ?? [];
				})
			);

			await Promise.all(promises);
		} catch (e) {
			console.error(e);
			toast.error('โหลดรายวิชาไม่สำเร็จ');
		} finally {
			addCourseLoading = false;
		}
	}

	async function handleAddCourse(subjectId: string) {
		if (!canManageCoursePlan) {
			toast.error('ไม่มีสิทธิ์เพิ่มรายวิชา');
			return;
		}

		if (!selectedClassroomId || !selectedTermId) return;
		addCourseSaving = subjectId;
		try {
			await assignCourses({
				classroom_id: selectedClassroomId,
				academic_semester_id: selectedTermId,
				subject_ids: [subjectId]
			});
			toast.success('เพิ่มรายวิชาแล้ว');
			await fetchCourses();
		} catch (e) {
			console.error(e);
			toast.error('เพิ่มไม่สำเร็จ');
		} finally {
			addCourseSaving = '';
		}
	}

	async function loadTeachers() {
		if (!canManageCoursePlan) return;

		if (teachersLoaded) return;
		try {
			const res = await lookupStaff({ activeOnly: true, limit: 1000 });
			teachers = res;
			teachersLoaded = true;
		} catch (e) {
			console.error('Failed to load teachers', e);
		}
	}

	async function openEditDialog(course: ClassroomCourse) {
		if (!canManageCoursePlan) {
			toast.error('ไม่มีสิทธิ์แก้ไขรายวิชา');
			return;
		}

		editingCourse = course;
		selectedTeacherId = course.primary_instructor_id || 'unassigned';
		if (!teachersLoaded) await loadTeachers();
		showEditDialog = true;
	}

	async function handleUpdateCourse() {
		if (!canManageCoursePlan) {
			toast.error('ไม่มีสิทธิ์แก้ไขรายวิชา');
			return;
		}

		if (!editingCourse) return;
		submitting = true;
		try {
			const teacherId = selectedTeacherId === 'unassigned' ? null : selectedTeacherId;
			await updateCourse(editingCourse.id, {
				primary_instructor_id: teacherId
			});
			toast.success('บันทึกข้อมูลสำเร็จ');
			showEditDialog = false;
			await fetchCourses();
		} catch (e) {
			console.error(e);
			toast.error('อัปเดตข้อมูลไม่สำเร็จ');
		} finally {
			submitting = false;
		}
	}

	// Watchers (Svelte 5 effects or just bind:value + handler)
	// Using handlers on Select.onValueChange is better for explicit control?
	// But shadcn Select binds value.

	// When Year Changes -> Fetch Classrooms, Reset Term & Class
	function onYearChange(id: string) {
		if (id !== selectedYearId) {
			selectedYearId = id;
			selectedTermId = '';
			selectedClassroomId = '';
			fetchClassrooms();
		}
	}

	// When Class/Term Changes -> Filter Courses
	// We can use $effect
	$effect(() => {
		if (canReadCoursePlan && selectedClassroomId && selectedTermId) {
