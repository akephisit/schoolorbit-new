	}

	async function handleExportActualCurriculum() {
		if (!canReadCoursePlan) {
			toast.error('ไม่มีสิทธิ์ส่งออกข้อมูลใช้จริง');
			return;
		}

		if (!actualExportYearId) {
			toast.error('กรุณาเลือกปีการศึกษา');
			return;
		}

		exportingActualCurriculum = true;
		try {
			const exportYear = structure.years.find((year) => year.id === actualExportYearId);
			const exportSemesters = structure.semesters.filter(
				(semester) => semester.academic_year_id === actualExportYearId
			);
			if (!exportYear || exportSemesters.length === 0) {
				toast.error('ไม่พบภาคเรียนในปีการศึกษาที่เลือก');
				return;
			}

			const exportClassrooms = (await listClassrooms({ year_id: actualExportYearId })).data ?? [];
			const courseGroups = await Promise.all(
				exportSemesters.map((semester) =>
					listClassroomCourses({ semesterId: semester.id }).then((res) => res.data ?? [])
				)
			);
			const exportCourses = courseGroups.flat();
			const courseInstructorsByCourseId = await batchListCourseInstructors(
				exportCourses.map((course) => course.id)
			).then((res) => res.data ?? {});
			const activityGroups = await Promise.all(
				exportClassrooms.flatMap((classroom) =>
					exportSemesters.map((semester) =>
						listClassroomActivities(classroom.id, semester.id).then((res) =>
							(res.data ?? []).map((activity) => ({
								...activity,
								classroom_id: classroom.id,
								semester_id: semester.id
							}))
						)
					)
				)
			);

			const rows = buildActualCurriculumRows({
				yearName: exportYear.name,
				semesters: exportSemesters,
				classrooms: exportClassrooms,
				courses: exportCourses,
				activities: activityGroups.flat() as ClassroomActivityExportItem[]
			});
			const subjectActivityRows = buildActualSubjectActivityRows({
				yearName: exportYear.name,
				semesters: exportSemesters,
				classrooms: exportClassrooms,
				courses: exportCourses,
				activities: activityGroups.flat() as ClassroomActivityExportItem[],
				courseInstructorsByCourseId
			});
			const summaryRows = summarizeActualCurriculum(rows);

			const XLSX = await import('xlsx');
			const workbook = XLSX.utils.book_new();
			XLSX.utils.book_append_sheet(
				workbook,
				XLSX.utils.json_to_sheet(actualRowsForWorksheet(rows)),
				'ใช้จริง'
			);
			const subjectActivityWorksheet = XLSX.utils.aoa_to_sheet(
				actualSubjectActivityReportForWorksheet(subjectActivityRows)
			);
			subjectActivityWorksheet['!cols'] = [
				{ wch: 14 },
				{ wch: 32 },
				{ wch: 14 },
				{ wch: 8 },
				{ wch: 8 },
				{ wch: 54 },
				{ wch: 42 }
			];
			XLSX.utils.book_append_sheet(workbook, subjectActivityWorksheet, 'รายวิชา-กิจกรรม');
			XLSX.utils.book_append_sheet(
				workbook,
				XLSX.utils.json_to_sheet(actualSummaryForWorksheet(summaryRows)),
				'สรุปห้องเรียน'
			);
			XLSX.writeFile(workbook, `รายวิชาที่ใช้จริง-${exportYear.name}.xlsx`);
			toast.success(`ส่งออกข้อมูลใช้จริง ${rows.length} รายการแล้ว`);
			showActualExportDialog = false;
		} catch (e) {
			console.error(e);
			toast.error('ส่งออกข้อมูลใช้จริงไม่สำเร็จ');
		} finally {
			exportingActualCurriculum = false;
		}
	}

	async function fetchClassrooms() {
		if (!canReadCoursePlan || !selectedYearId) return;
		try {
			const res = await listClassrooms({ year_id: selectedYearId });
			classrooms = res.data;
			selectedClassroomId = ''; // Reset class when year changes
		} catch (e) {
			console.error(e);
			toast.error('โหลดห้องเรียนไม่สำเร็จ');
		}
	}

	async function fetchCourses() {
		if (!canReadCoursePlan || !selectedClassroomId || !selectedTermId) return;
		try {
			loading = true;
			const res = await listClassroomCourses(selectedClassroomId, selectedTermId);
			courses = res.data;
		} catch (e) {
			console.error(e);
			toast.error('โหลดข้อมูลรายวิชาไม่สำเร็จ');
		} finally {
			loading = false;
		}
	}

	async function fetchClassroomActivities() {
		if (!canReadCoursePlan) return;

		if (!selectedClassroomId || !selectedTermId) {
			classroomActivities = [];
			return;
		}
		try {
			loadingActivities = true;
			const res = await listClassroomActivities(selectedClassroomId, selectedTermId);
			classroomActivities = res.data ?? [];
		} catch (e) {
			console.error(e);
			classroomActivities = [];
		} finally {
			loadingActivities = false;
		}
	}

