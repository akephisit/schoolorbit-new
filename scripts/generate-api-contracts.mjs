import { execFile } from "node:child_process";
import { randomUUID } from "node:crypto";
import {
  mkdtemp,
  mkdir,
  readFile,
  rename,
  rm,
  writeFile,
} from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);
const scriptPath = fileURLToPath(import.meta.url);
const defaultRepositoryRoot = path.resolve(path.dirname(scriptPath), "..");

function sortValue(value) {
  if (Array.isArray(value)) return value.map(sortValue);
  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value)
        .sort(([left], [right]) => left.localeCompare(right, "en"))
        .map(([key, child]) => [key, sortValue(child)]),
    );
  }
  return value;
}

function renderOpenApi(document) {
  if (!document || document.openapi !== "3.1.0") {
    throw new Error("backend-school exporter must return OpenAPI 3.1.0");
  }
  return `${JSON.stringify(sortValue(document), null, 2)}\n`;
}

function renderGeneratedTypes(rawTypes) {
  const withoutDefaultHeader = rawTypes.replace(
    /^\/\/ This file was auto-generated by openapi-typescript\.\s*/,
    "",
  );
  const output = `// @generated by scripts/generate-api-contracts.mjs; DO NOT EDIT.\n${withoutDefaultHeader}`;
  return output.endsWith("\n") ? output : `${output}\n`;
}

async function readIfPresent(filePath) {
  try {
    return await readFile(filePath, "utf8");
  } catch (error) {
    if (error?.code === "ENOENT") return null;
    throw error;
  }
}

async function defaultExportSchoolDocument(repositoryRoot) {
  const { stdout } = await execFileAsync(
    "cargo",
    [
      "run",
      "--quiet",
      "--manifest-path",
      path.join(repositoryRoot, "backend-school/Cargo.toml"),
      "--bin",
      "backend-school",
      "--",
      "export-openapi",
    ],
    {
      cwd: repositoryRoot,
      maxBuffer: 16 * 1024 * 1024,
    },
  );
  return JSON.parse(stdout);
}

async function defaultGenerateSchoolTypes(repositoryRoot, document) {
  const temporaryDirectory = await mkdtemp(
    path.join(os.tmpdir(), "schoolorbit-openapi-"),
  );
  try {
    const inputPath = path.join(temporaryDirectory, "school-api.json");
    const outputPath = path.join(temporaryDirectory, "school-api.ts");
    await writeFile(inputPath, renderOpenApi(document));

    const executable = path.join(
      repositoryRoot,
      "frontend-school/node_modules/.bin",
      process.platform === "win32"
        ? "openapi-typescript.cmd"
        : "openapi-typescript",
    );
    const prettierExecutable = path.join(
      repositoryRoot,
      "frontend-school/node_modules/.bin",
      process.platform === "win32" ? "prettier.cmd" : "prettier",
    );
    await execFileAsync(
      executable,
      [inputPath, "--output", outputPath, "--alphabetize"],
      {
        cwd: path.join(repositoryRoot, "frontend-school"),
        maxBuffer: 16 * 1024 * 1024,
      },
    );
    await execFileAsync(
      prettierExecutable,
      [
        outputPath,
        "--write",
        "--config",
        path.join(repositoryRoot, "frontend-school/.prettierrc"),
      ],
      {
        cwd: path.join(repositoryRoot, "frontend-school"),
        maxBuffer: 16 * 1024 * 1024,
      },
    );

    return await readFile(outputPath, "utf8");
  } finally {
    await rm(temporaryDirectory, { recursive: true, force: true });
  }
}

export async function commitGeneratedOutputs(
  outputs,
  { renameFile = rename } = {},
) {
  const transactionId = randomUUID();
  const entries = outputs.map(([filePath, output], index) => ({
    filePath,
    output,
    stagedPath: `${filePath}.api-contract-${transactionId}-${index}.tmp`,
    backupPath: `${filePath}.api-contract-${transactionId}-${index}.bak`,
    hasStage: false,
    hasBackup: false,
    installed: false,
  }));

  try {
    for (const entry of entries) {
      await mkdir(path.dirname(entry.filePath), { recursive: true });
      entry.hasStage = true;
      await writeFile(entry.stagedPath, entry.output, { flag: "wx" });
    }

    for (const entry of entries) {
      if ((await readIfPresent(entry.filePath)) !== null) {
        await renameFile(entry.filePath, entry.backupPath);
        entry.hasBackup = true;
      }

      await renameFile(entry.stagedPath, entry.filePath);
      entry.hasStage = false;
      entry.installed = true;
    }
  } catch (error) {
    const rollbackErrors = [];
    for (const entry of entries.toReversed()) {
      if (entry.installed) {
        try {
          await rm(entry.filePath, { force: true });
          entry.installed = false;
        } catch (rollbackError) {
          rollbackErrors.push(rollbackError);
        }
      }
      if (entry.hasBackup) {
        try {
          await renameFile(entry.backupPath, entry.filePath);
          entry.hasBackup = false;
        } catch (rollbackError) {
          rollbackErrors.push(rollbackError);
        }
      }
      if (entry.hasStage) {
        try {
          await rm(entry.stagedPath, { force: true });
          entry.hasStage = false;
        } catch (rollbackError) {
          rollbackErrors.push(rollbackError);
        }
      }
    }

    if (rollbackErrors.length > 0) {
      throw new AggregateError(
        [error, ...rollbackErrors],
        "API contract output replacement and rollback failed",
      );
    }
    throw error;
  }

  for (const entry of entries) {
    if (entry.hasBackup) await rm(entry.backupPath, { force: true });
    if (entry.hasStage) await rm(entry.stagedPath, { force: true });
  }
}

export async function generateApiContracts({
  repositoryRoot = defaultRepositoryRoot,
  check = false,
  exportSchoolDocument = defaultExportSchoolDocument,
  generateSchoolTypes = defaultGenerateSchoolTypes,
} = {}) {
  const document = await exportSchoolDocument(repositoryRoot);
  const openApiOutput = renderOpenApi(document);
  const rawTypes = await generateSchoolTypes(repositoryRoot, document);
  const typeOutput = renderGeneratedTypes(rawTypes);
  const outputs = [
    [
      path.join(repositoryRoot, "contracts/openapi/school-api.json"),
      openApiOutput,
    ],
    [
      path.join(
        repositoryRoot,
        "frontend-school/src/lib/api/generated/school-api.ts",
      ),
      typeOutput,
    ],
  ];

  if (check) {
    const stale = [];
    for (const [filePath, expected] of outputs) {
      if ((await readIfPresent(filePath)) !== expected) {
        stale.push(path.relative(repositoryRoot, filePath));
      }
    }
    if (stale.length > 0) {
      throw new Error(
        `stale generated API contract artifacts: ${stale.join(", ")}`,
      );
    }
    return;
  }

  await commitGeneratedOutputs(outputs);
}

const invokedDirectly =
  process.argv[1] && path.resolve(process.argv[1]) === scriptPath;
if (invokedDirectly) {
  const args = process.argv.slice(2);
  const validArguments =
    args.length === 0 || (args.length === 1 && args[0] === "--check");

  if (!validArguments) {
    console.error("usage: node scripts/generate-api-contracts.mjs [--check]");
    process.exitCode = 2;
  } else {
    generateApiContracts({ check: args[0] === "--check" }).catch((error) => {
      console.error(error instanceof Error ? error.message : String(error));
      process.exitCode = 1;
    });
  }
}
